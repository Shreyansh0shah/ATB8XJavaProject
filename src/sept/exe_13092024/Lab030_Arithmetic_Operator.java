package sept.exe_13092024;

public class Lab030_Arithmetic_Operator {
    public static void main(String[] args) {

        // arithmetic Operator
        //+,-,/,*
        int a = 10;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // % - Modulus - Remainders
        System.out.println(a%b);

     //   2 | 10 | 5 -Quotient
      //    | 10 |
        // ---------
        //   0 -Remainder


    }
}
