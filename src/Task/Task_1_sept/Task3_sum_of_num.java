package Task.Task_1_sept;

public class Task3_sum_of_num {
    public static void main(String[] args) {
        // Calculate the sum of numbers from 1 to 100
        int sum =0;
        int i =1;
      do{
            sum+=i;
            i++;
        } while(i<=100);
        System.out.println(sum);

    }
}
